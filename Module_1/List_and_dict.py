# Работа со списком
my_list = ['red', 'blue', 'black', 'white', 'orange', 'green']
print('My list = ', my_list)

print('First element list = ', my_list[0])
print('Last element list = ', my_list[-1])
print('My sublist = ', my_list[2:5])
my_list[2] = 'yellow'
print('My new list = ', my_list)

# Работа со словарем
my_dict = {'red': 'красный', 'blue': 'голубой', 'black': 'черный', 'white': 'белый'}
print('My dictionary = ', my_dict)
print('Value my dictionare with key = ', my_dict['black'])
my_dict['blue'] = 'синий'
my_dict['green'] = 'зеленый'
print('My new dictionary = ', my_dict)
